                    (string)this.Celdas[level, celda, 2],
                    (int)this.Celdas[level, celda, 3],
                    (int)this.Celdas[level, celda, 4]);

            for (int muro = 0; muro < 129 && (int)this.Murallas[level, muro, 0] != -1; muro++)
                Link.THE_FORM.tablero.CreateMuralla((int)this.Murallas[level, muro, 0], (int)this.Murallas[level, muro, 1]);

            Link.THE_FORM.tablero.Celdas.CurrentCelda = Link.THE_FORM.tablero.Celdas[0];
